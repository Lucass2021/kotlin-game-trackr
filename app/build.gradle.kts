fun apiBaseUrl(buildType: String): String {
    val props = Properties()
    rootProject.file("config/$buildType.properties")
        .takeIf { it.exists() }?.inputStream()?.use { props.load(it) }
    rootProject.file("config/local.properties")
        .takeIf { it.exists() }?.inputStream()?.use { props.load(it) }
    return props.getProperty("API_BASE_URL") ?: "http://10.0.2.2:8000/api"
}

